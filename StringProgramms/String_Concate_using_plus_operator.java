class String_Concate_using_plus_operator
{
    public static void main(String[] args) {
        String s1="java";
        String s2="Programming";

        System.out.println("-----------------------");
        System.out.println(s1+s2);

        System.out.println(s1+10);

        System.out.println(s1+10+20);

        System.out.println(20+10+s1);

        System.out.println(20+s1+10);

        System.out.println(s1+20/10);

        System.out.println(s1+20*20);
        
        //System.out.println(s1+20-5); error

    }
}